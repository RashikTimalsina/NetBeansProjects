    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        try {
            pst=conn.prepareStatement("select * from customer where roomnumber=? and status=?");
            pst.setString(1,txtroomnumber.getText());
            pst.setString(2,"NULL");
            rs=pst.executeQuery();
            
            if(rs.next()){
                txtname.setText(rs.getString("name"));
                txtmobile.setText(rs.getString("mobile"));
                txtid.setText(rs.getString("id"));
                txtpriceper.setText(rs.getString("price"));
                txtcheckindate.setText(rs.getString("joindate"));
                
            }
            
            String s1,s2;
            s1=txtcheckoutdate.getText();
            s2=rs.getString("joindate");
            SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd");
            
            Date d1=format.parse(s1);
            Date d2=format.parse(s2);
            long diff=d1.getTime()-d2.getTime();
            int days=(int)diff/(24*60*60*1000);
            if(days==0)
                days=1;
            txtnumberdays.setText(String.valueOf(days));
            double price=Double.parseDouble(rs.getString("price"))*days;
            txttotal.setText(String .valueOf(price));
            
        } catch (SQLException ex) {
            Logger.getLogger(Checkout.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(Checkout.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsearchActionPerformed

