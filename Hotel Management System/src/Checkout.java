    public void ShowDataToTable(){
        try {
            pst=conn.prepareStatement("select * from customer where status=?");
            pst.setString(1,"NULL");
            rs=pst.executeQuery();
            ResultSetMetaData rsmd=(ResultSetMetaData) rs.getMetaData();
            int n=rsmd.getColumnCount();
            DefaultTableModel df=(DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for(int i=1;i<=n;i++){
                    
                    v.add(rs.getString("name"));   
                     v.add(rs.getString("mobile")); 
                     v.add(rs.getString("id")); 
                      v.add(rs.getString("roomnumber")); 
                       v.add(rs.getString("bed")); 
                        v.add(rs.getString("roomtype"));
                        v.add(rs.getString("joindate"));
                         v.add(rs.getString("price")); 
                          v.add(rs.getString("nationality")); 
                }
                df.addRow(v); 
            
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Checkout.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }

