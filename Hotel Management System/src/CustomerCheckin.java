    private void btncheckinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncheckinActionPerformed
        if(txtname.getText().equals("")){
            txtname.requestFocus();
            JOptionPane.showMessageDialog(this,"All field is required");
            
        }
        else if(txtmobile.getText().equals("")){
            txtmobile.requestFocus();
            JOptionPane.showMessageDialog(this,"All field is required");
            
        }
        else if(txtnationality.getText().equals("")){
            txtnationality.requestFocus();
            JOptionPane.showMessageDialog(this,"All field is required");
            
        }
        else if(txtcitizenship.getText().equals("")){
            txtcitizenship.requestFocus();
            JOptionPane.showMessageDialog(this,"All field is required");
            
        }
          else{
            try {
                pst=conn.prepareStatement("insert into customer(name,mobile,gender,nationality,id,joindate,bed,roomtype,roomnumber,price,status)values(?,?,?,?,?,?,?,?,?,?,?)"); 
                pst.setString(1,txtname.getText());  
                pst.setString(2,txtmobile.getText());
                pst.setString(3,cmbgender.getItemAt(cmbgender.getSelectedIndex()));
                pst.setString(4,txtnationality.getText());
                pst.setString(5,txtcitizenship.getText());
                pst.setString(6,txtdate.getText());
                pst.setString(7,cmbbedtype.getItemAt(cmbbedtype.getSelectedIndex()));
                pst.setString(8,cmbroomtype.getItemAt(cmbroomtype.getSelectedIndex()));
                pst.setString(9,cmbroomnumber.getItemAt(cmbroomnumber.getSelectedIndex()));
                pst.setString(10,txtprice.getText());
                pst.setString(11,"NULL");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Customer Checked-In Successfully ");
                txtname.setText("");
                txtcitizenship.setText("");
                txtmobile.setText("");
                txtnationality.setText("");
                
                pst=conn.prepareStatement("update room set status=? where roomnumber=?");
                pst.setString(1,"BOOKED");
                pst.setString(2,cmbroomnumber.getItemAt(cmbroomnumber.getSelectedIndex()));
                pst.executeUpdate();
                
                Rashik();
                     
            } catch (SQLException ex) {
                Logger.getLogger(CustomerCheckin.class.getName()).log(Level.SEVERE, null, ex);
            }
          
          
          }
    }//GEN-LAST:event_btncheckinActionPerformed

