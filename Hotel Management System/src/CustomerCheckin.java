    public void Rashik()
    {
        try {
            pst=conn.prepareStatement("select Roomnumber from room where Bed=? and Roomtype=? and Status=?");
            pst.setString(1,cmbbedtype.getItemAt(cmbbedtype.getSelectedIndex()));
            pst.setString(2,cmbroomtype.getItemAt(cmbroomtype.getSelectedIndex())); 
            pst.setString(3,"NOT-BOOKED");
            rs=pst.executeQuery();
            cmbroomnumber.removeAllItems();
            while(rs.next()){
                cmbroomnumber.addItem(rs.getString("Roomnumber"));
            }
             pst=conn.prepareStatement("select Price from room where Roomnumber=?");
             pst.setString(1,cmbroomnumber.getItemAt(cmbroomnumber.getSelectedIndex()));
             rs=pst.executeQuery();
             
             if(rs.next()){
                             txtprice.setText(rs.getString("Price"));
        }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerCheckin.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }
